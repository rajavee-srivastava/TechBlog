@api_view(['GET'])
def get_blog(request, key):
    try:
        blog = Blog.objects.get(pk=key)
    except Blog.DoesNotExist:
        return Response({"error": "Blog entry not found."}, status=404)

    serializer = BlogSerializer(blog)
    return Response(serializer.data)


